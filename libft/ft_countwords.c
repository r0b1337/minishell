#include "libft.h"

int		ft_countwords(char const *s, char c)
{
	int words;
	int i;

	i = 0;
	words = 0;
	while (s[i])
	{
		if (s[i] != c && (s[i - 1] == c || i == 0))
			words++;
		i++;
	}
	return (words);
}
